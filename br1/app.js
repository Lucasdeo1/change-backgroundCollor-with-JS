function changecollor(){
    var a = document.getElementById('mouse')
    a.style.backgroundColor = 'blue'
    a.display = 'inline-block'  
    a.padding = '10px'
    a.style.backgroundColor = 'red'  
}

function deschangecollor(){
    var a = document.getElementById('mouse')
    a.style.backgroundColor = 'blue'
    a.display = 'inline-block'  
    a.padding = '10px'
}
